# Function to calculate logarithmic ticks
def log_ticks(x, base):
    start = np.floor(np.log10(x.min()) / np.log10(base))
    end = np.ceil(np.log10(x.max()) / np.log10(base))
    return np.logspace(start, end, int((end - start) + 1), base=base)


